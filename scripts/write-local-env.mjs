#!/usr/bin/env node

import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

import { nsecEncode } from 'nostr-tools/nip19'

import { hexToBytes, localDevAccounts } from './local-dev-keys.mjs'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const appDir = resolve(root, 'dependencies/marketplace-app-ts')
const cashuConfigPath = resolve(root, 'dependencies/marketplace-cashu-stack/data/config/marketplace-cashu-stack.json')
const evmConfigPath = resolve(root, 'dependencies/marketplace-evm-stack/data/config/marketplace-evm-stack.json')

function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf8'))
}

function envLine(key, value) {
  return `${key}=${typeof value === 'string' ? value : JSON.stringify(value)}`
}

const demoAccountLabels = Object.freeze({
  buyer: 'Buyer',
  buyerOne: 'Buyer One',
  buyerTwo: 'Buyer Two',
  sellerOpen: 'Seller - no methods',
  sellerEvm: 'Seller - EVM',
  sellerCashu: 'Seller - Cashu',
  sellerBoth: 'Seller - EVM + Cashu',
  arbiterEvm: 'Arbiter - EVM',
  arbiterCashu: 'Arbiter - Cashu',
  arbiterBoth: 'Arbiter - EVM + Cashu',
})

const publicUrls = {
  relay: process.env.NMDK_PUBLIC_RELAY || 'wss://relay.marketplace.test',
  signet: process.env.NMDK_PUBLIC_SIGNET_URL || 'https://signet.marketplace.test',
  blossomUpload: process.env.NMDK_PUBLIC_BLOSSOM_UPLOAD_URL || 'https://blossom.marketplace.test/upload',
  arbitrumRpc: process.env.NMDK_PUBLIC_ARBITRUM_RPC_URL || 'https://arbitrum.evm.marketplace.test',
  arbitrumExplorer: process.env.NMDK_PUBLIC_ARBITRUM_EXPLORER_URL || 'https://explorer.arbitrum.evm.marketplace.test',
  boltzApi: process.env.NMDK_PUBLIC_BOLTZ_API_URL || 'https://boltz.evm.marketplace.test/v2',
  bundler: process.env.NMDK_PUBLIC_BUNDLER_URL || 'https://bundler.evm.marketplace.test',
  paymaster: process.env.NMDK_PUBLIC_PAYMASTER_URL || 'https://paymaster.evm.marketplace.test',
  lnbits: process.env.NMDK_PUBLIC_LNBITS_URL || 'https://lnbits.marketplace.test',
  cashuSatMint: process.env.NMDK_PUBLIC_CASHU_MINT_URL || 'https://mint.cashu.marketplace.test',
  cashuUsdMint: process.env.NMDK_PUBLIC_CASHU_USD_MINT_URL || 'https://mint-usd.cashu.marketplace.test',
}

const cashu = readJson(cashuConfigPath)
const evm = readJson(evmConfigPath)
const arbitrum = evm.chains.arbitrumRegtest
const aa = arbitrum.accountAbstraction

const tbtcAsset = Object.values(arbitrum.assets).find(asset => asset.boltzCurrency?.toUpperCase() === 'TBTC')
const assets = Object.values(arbitrum.assets).map(asset => ({
  denomination: asset.denomination,
  address: asset.address,
  decimals: asset.decimals,
  ...(asset.boltzCurrency ? { boltzCurrency: asset.boltzCurrency } : {}),
  ...(asset.boltzCurrency?.toUpperCase() === 'USDT' && tbtcAsset && arbitrum.boltzCurrency
    ? {
        boltzRouteVia: {
          boltzCurrency: tbtcAsset.boltzCurrency,
          assetAddress: tbtcAsset.address,
          decimals: tbtcAsset.decimals,
          quoteCurrency: arbitrum.boltzCurrency,
        },
      }
    : {}),
}))

const cashuMints = Object.entries(cashu.cashu.mints).map(([key, mint]) => ({
  mintUrl: key === 'usd' ? publicUrls.cashuUsdMint : publicUrls.cashuSatMint,
  unit: mint.unit,
  denomination: mint.denomination ?? key.toUpperCase(),
  decimals: mint.decimals ?? (mint.unit === 'usd' ? 2 : 0),
}))

const demoAccounts = Object.entries(localDevAccounts).map(([id, account]) => ({
  id,
  label: demoAccountLabels[id] ?? id,
  nsec: nsecEncode(hexToBytes(account.privateKey)),
}))

const lines = [
  '# Generated by `npm run up`.',
  '# Localhost-only NMDK stack config; delete this file to fall back to .env.development.',
  '',
  envLine('VITE_RELAYS', publicUrls.relay),
  envLine('VITE_NIP46_RELAYS', publicUrls.relay),
  envLine('VITE_SIGNET_URL', publicUrls.signet),
  envLine('VITE_BLOSSOM_UPLOAD_URL', publicUrls.blossomUpload),
  envLine('VITE_DEMO_ACCOUNTS', demoAccounts),
  envLine('VITE_MARKETPLACE_AUTO_TRUST_ARBITER_PUBKEYS', [
    localDevAccounts.arbiterEvm.pubkey,
    localDevAccounts.arbiterCashu.pubkey,
    localDevAccounts.arbiterBoth.pubkey,
  ].join(',')),
  '',
  envLine('VITE_EVM_CHAIN_ID', String(arbitrum.chainId)),
  envLine('VITE_EVM_CHAIN_NAME', arbitrum.name),
  envLine('VITE_EVM_BOLTZ_CURRENCY', arbitrum.boltzCurrency),
  envLine('VITE_EVM_RPC_URL', publicUrls.arbitrumRpc),
  envLine('VITE_EVM_BLOCK_EXPLORER_URL', publicUrls.arbitrumExplorer),
  envLine('VITE_EVM_BOLTZ_API_URL', publicUrls.boltzApi),
  envLine('VITE_EVM_ENTRY_POINT_ADDRESS', aa.entryPointAddress),
  envLine('VITE_EVM_ACCOUNT_FACTORY_ADDRESS', aa.factoryAddress),
  envLine('VITE_EVM_BUNDLER_URL', publicUrls.bundler),
  envLine('VITE_EVM_PAYMASTER_URL', publicUrls.paymaster),
  envLine('VITE_EVM_PAYMASTER_ADDRESS', aa.paymasterAddress),
  envLine('VITE_EVM_MULTI_ESCROW_ADDRESS', arbitrum.multiEscrow.address),
  envLine('VITE_EVM_MULTI_ESCROW_BYTECODE_HASH', arbitrum.multiEscrow.runtimeBytecodeHash),
  envLine('VITE_EVM_ARBITER_ADDRESS', evm.accounts.arbiter.address),
  envLine('VITE_EVM_ARBITER_NOSTR_PUBKEY', localDevAccounts.arbiterEvm.pubkey),
  envLine('VITE_EVM_ASSETS', assets),
  '',
  envLine('VITE_LNBITS_URL', publicUrls.lnbits),
  '',
  envLine('VITE_CASHU_MINT_URL', publicUrls.cashuSatMint),
  envLine('VITE_CASHU_MINTS', cashuMints),
  envLine('MARKETPLACE_CASHU_PUBLIC_MINT_URL', publicUrls.cashuSatMint),
  envLine('MARKETPLACE_CASHU_PUBLIC_USD_MINT_URL', cashu.cashu.mints.usd ? publicUrls.cashuUsdMint : ''),
  envLine('MARKETPLACE_CASHU_MINTS', cashuMints),
  '',
  envLine('MARKETPLACE_EVM_STACK_CONFIG', evmConfigPath),
  envLine('MARKETPLACE_CASHU_STACK_CONFIG', cashuConfigPath),
]

const appEnvPath = resolve(appDir, '.env.local')
writeFileSync(appEnvPath, `${lines.join('\n')}\n`)

const appDevelopmentEnvPath = resolve(appDir, '.env.development.local')
writeFileSync(appDevelopmentEnvPath, `${lines.join('\n')}\n`)

const shellEnvPath = resolve(root, '.nmdk.local.env')
writeFileSync(shellEnvPath, `${lines.filter(line => line && !line.startsWith('#')).join('\n')}\n`)

console.log(`Wrote ${appEnvPath}`)
console.log(`Wrote ${appDevelopmentEnvPath}`)
console.log(`Wrote ${shellEnvPath}`)
