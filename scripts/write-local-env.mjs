#!/usr/bin/env node

import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

import { nsecEncode } from 'nostr-tools/nip19'

import { hexToBytes, localDevAccounts } from './local-dev-keys.mjs'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const appDir = resolve(root, 'dependencies/marketplace-app-ts')
const cashuConfigPath = resolve(root, 'dependencies/marketplace-cashu-stack/data/config/marketplace-cashu-stack.json')
const evmConfigPath = resolve(root, 'dependencies/marketplace-evm-stack/data/config/marketplace-evm-stack.json')

function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf8'))
}

function envLine(key, value) {
  return `${key}=${typeof value === 'string' ? value : JSON.stringify(value)}`
}

const demoAccountLabels = Object.freeze({
  buyer: 'Buyer',
  sellerOpen: 'Seller - no methods',
  sellerEvm: 'Seller - EVM',
  sellerCashu: 'Seller - Cashu',
  sellerBoth: 'Seller - EVM + Cashu',
  escrowEvm: 'Escrow - EVM',
  escrowCashu: 'Escrow - Cashu',
  escrowBoth: 'Escrow - EVM + Cashu',
})

const cashu = readJson(cashuConfigPath)
const evm = readJson(evmConfigPath)
const arbitrum = evm.chains.arbitrumRegtest
const aa = arbitrum.accountAbstraction

const assets = Object.values(arbitrum.assets).map(asset => ({
  denomination: asset.denomination,
  address: asset.address,
  decimals: asset.decimals,
  ...(asset.boltzCurrency ? { boltzCurrency: asset.boltzCurrency } : {}),
}))

const cashuMints = Object.entries(cashu.cashu.mints).map(([key, mint]) => ({
  mintUrl: mint.url,
  unit: mint.unit,
  denomination: mint.denomination ?? key.toUpperCase(),
  decimals: mint.decimals ?? (mint.unit === 'usd' ? 2 : 0),
}))

const demoAccounts = Object.entries(localDevAccounts).map(([id, account]) => ({
  id,
  label: demoAccountLabels[id] ?? id,
  nsec: nsecEncode(hexToBytes(account.privateKey)),
}))

const lines = [
  '# Generated by `npm run up`.',
  '# Localhost-only NMDK stack config; delete this file to fall back to .env.development.',
  '',
  envLine('VITE_RELAYS', cashu.nostr.relayUrl),
  envLine('VITE_NIP46_RELAYS', [cashu.nostr.relayUrl, 'ws://host.docker.internal:18080'].join(',')),
  envLine('VITE_SIGNET_URL', 'http://127.0.0.1:13047'),
  envLine('VITE_DEMO_ACCOUNTS', demoAccounts),
  envLine('VITE_MARKETPLACE_AUTO_TRUST_ESCROW_PUBKEYS', [
    localDevAccounts.escrowEvm.pubkey,
    localDevAccounts.escrowCashu.pubkey,
    localDevAccounts.escrowBoth.pubkey,
  ].join(',')),
  '',
  envLine('VITE_EVM_CHAIN_ID', String(arbitrum.chainId)),
  envLine('VITE_EVM_CHAIN_NAME', arbitrum.name),
  envLine('VITE_EVM_RPC_URL', arbitrum.rpcUrl),
  envLine('VITE_EVM_BOLTZ_API_URL', evm.boltz.apiUrl),
  envLine('VITE_EVM_ENTRY_POINT_ADDRESS', aa.entryPointAddress),
  envLine('VITE_EVM_ACCOUNT_FACTORY_ADDRESS', aa.factoryAddress),
  envLine('VITE_EVM_BUNDLER_URL', aa.bundlerUrl),
  envLine('VITE_EVM_PAYMASTER_URL', aa.paymasterUrl),
  envLine('VITE_EVM_PAYMASTER_ADDRESS', aa.paymasterAddress),
  envLine('VITE_EVM_MULTI_ESCROW_ADDRESS', arbitrum.multiEscrow.address),
  envLine('VITE_EVM_MULTI_ESCROW_BYTECODE_HASH', arbitrum.multiEscrow.runtimeBytecodeHash),
  envLine('VITE_EVM_ARBITER_ADDRESS', evm.accounts.arbiter.address),
  envLine('VITE_EVM_ARBITER_NOSTR_PUBKEY', localDevAccounts.escrowEvm.pubkey),
  envLine('VITE_EVM_ASSETS', assets),
  '',
  envLine('VITE_CASHU_MINT_URL', cashu.cashu.mints.sat.url),
  envLine('VITE_CASHU_MINTS', cashuMints),
  envLine('MARKETPLACE_CASHU_PUBLIC_MINT_URL', cashu.cashu.mints.sat.url),
  envLine('MARKETPLACE_CASHU_PUBLIC_USD_MINT_URL', cashu.cashu.mints.usd?.url ?? ''),
  envLine('MARKETPLACE_CASHU_MINTS', cashuMints),
  '',
  envLine('MARKETPLACE_EVM_STACK_CONFIG', evmConfigPath),
  envLine('MARKETPLACE_CASHU_STACK_CONFIG', cashuConfigPath),
]

const appEnvPath = resolve(appDir, '.env.local')
writeFileSync(appEnvPath, `${lines.join('\n')}\n`)

const appDevelopmentEnvPath = resolve(appDir, '.env.development.local')
writeFileSync(appDevelopmentEnvPath, `${lines.join('\n')}\n`)

const shellEnvPath = resolve(root, '.nmdk.local.env')
writeFileSync(shellEnvPath, `${lines.filter(line => line && !line.startsWith('#')).join('\n')}\n`)

console.log(`Wrote ${appEnvPath}`)
console.log(`Wrote ${appDevelopmentEnvPath}`)
console.log(`Wrote ${shellEnvPath}`)
